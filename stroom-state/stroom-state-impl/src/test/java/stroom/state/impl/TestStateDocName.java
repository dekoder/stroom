package stroom.state.impl;

import org.junit.jupiter.api.Test;

import java.util.Set;

import static org.assertj.core.api.Assertions.assertThat;

public class TestStateDocName {

    @Test
    void testUniqueName() {
        assertThat(StateDocStoreImpl.createUniqueName("test",
                Set.of("test"))).isEqualTo("test2");
        assertThat(StateDocStoreImpl.createUniqueName("test2",
                Set.of("test"))).isEqualTo("test3");
        assertThat(StateDocStoreImpl.createUniqueName("test2",
                Set.of("test", "test2", "test3"))).isEqualTo("test4");
    }
}
